#include <config.h>
#include <mdsshr.h>
#include "mdsip_connections.h"

static pthread_mutex_t ast_mutex = PTHREAD_MUTEX_INITIALIZER;

////////////////////////////////////////////////////////////////////////////////
//  LockAsts  //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

void LockAsts(){
  pthread_mutex_lock(&ast_mutex);
}

///
/// Unlocks AST mutex
///
void UnlockAsts(){
  pthread_mutex_unlock(&ast_mutex);
}
